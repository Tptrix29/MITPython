def get_permutations(sequence):
    '''
    Enumerate all permutations of a given string

    sequence (string): an arbitrary string to permute. Assume that it is a
    non-empty string.  

    You MUST use recursion for this part. Non-recursive solutions will not be
    accepted.

    Returns: a list of all permutations of sequence

    Example:
    >>> get_permutations('abc')
    ['abc', 'acb', 'bac', 'bca', 'cab', 'cba']

    Note: depending on your implementation, you may return the permutations in
    a different order than what is listed here.
    '''
    pm_list = []
    l = len(sequence)
    if l == 1:
        pm_list = list(sequence)
    else:
        pre_pm_list = get_permutations(sequence[1:])
        add = sequence[0]
        for example in pre_pm_list:
            pm_list.append(add + example)
            pm_list.extend([example[:i] + add + example[i:] for i in range(l)])
            pm_list.append(example + add)
    pm_list = list(set(pm_list))

    return pm_list

